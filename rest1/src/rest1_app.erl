-module(rest1_app).

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    Dispatch = cowboy_router:compile([
		{'_',[
			{"/", rest1_handler, []}
		]}
    ]),
    {ok, _} = cowboy:start_http(http, 200, [{port, 8000}],
			[{env, [{dispatch, Dispatch}]}
    ]),
    rest1_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

